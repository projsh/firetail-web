    public Set<String> listFilesUsingDirectoryStream(String dir) throws IOException {
        Set<String> fileList = new HashSet<>();
        try (DirectoryStream<Path> stream = Files.newDirectoryStream(Paths.get(dir))) {
            for (Path path : stream) {
                if (!Files.isDirectory(path)) {
                    fileList.add(path.getFileName().toString());
                }
            }
        }
        return fileList;
    }
    
    public MainUI() {
        initComponents();
        setLocationRelativeTo(null);
        addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosing(WindowEvent ev) {
                setState(ICONIFIED);
            }
        });
        resourceField.setText(FiretailWeb.musLoc);
        mainTabPane.addChangeListener(e -> {
            JTabbedPane pane = (JTabbedPane) e.getSource();
            switch (pane.getTitleAt(pane.getSelectedIndex())) {
                case "Songs":
                    Thread lol = new Thread(() -> {
                        try {
                            URL url = new URL("http://localhost:8080/api/getfiles");
                            HttpURLConnection con = (HttpURLConnection) url.openConnection();
                            con.setRequestMethod("GET");
                            BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream()));
                            String inputLine;
                            StringBuilder content = new StringBuilder();
                            while ((inputLine = in.readLine()) != null) {
                                content.append(inputLine);
                            }
                            in.close();
                            con.disconnect();
                            String fin = content.toString();
                            fin = fin.substring(2, (int) fin.length() - 2);
                            String[] finAr = fin.split("\"");
                            List<String> finL = List.of(finAr);
                            int index = 0;
                            DefaultListModel listModel = new DefaultListModel();
                            for (String el : finL) {
                                if (!el.equals(",")) {
                                    listModel.addElement(finL.get(index));
                                }
                                index++;
                            }
                            listSongs.setModel(listModel);
                        } catch(IOException err) {

                        }
                    });
                    lol.start();
                break;
                case "Settings":
                    Thread getDir = new Thread(() -> {
                        try {
                            URL url = new URL("http://localhost:8080/api/currentDir");
                            HttpURLConnection con = (HttpURLConnection) url.openConnection();
                            con.setRequestMethod("GET");
                            BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream()));
                            String inputLine;
                            StringBuilder content = new StringBuilder();
                            while ((inputLine = in.readLine()) != null) {
                                content.append(inputLine);
                            }
                            in.close();
                            con.disconnect();
                            resourceField.setText(content.toString());
                        } catch(IOException err) {
                            
                        }
                    });
                    getDir.start();
                break;
            }
        });
    }

